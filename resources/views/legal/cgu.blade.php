    <p>L'ensemble des éléments de l'Application (logos, design, code source, contenus) sont la propriété exclusive de YUPI Global. Toute reproduction non autorisée est interdite.</p>

    <h2>13. Suppression de compte</h2>
    <p>L'utilisateur peut demander la suppression de son compte à tout moment depuis l'Application (Compte → Supprimer mon compte) ou en contactant <a href="mailto:support@kekenon.com">support@kekenon.com</a>. La suppression est irréversible et entraîne la perte de toutes les données associées.</p>

    <h2>14. Modification des CGU</h2>
    <p>Kêkênon se réserve le droit de modifier les présentes CGU. Les utilisateurs seront informés par notification dans l'Application. La poursuite de l'utilisation après modification vaut acceptation.</p>

    <h2>15. Droit applicable</h2>
    <p>Les présentes CGU sont régies par le droit béninois. Tout litige sera soumis aux tribunaux compétents de Cotonou, après tentative de résolution amiable.</p>

