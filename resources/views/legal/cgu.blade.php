    <h2>1. Objet</h2>
    <p>Les présentes Conditions Générales d'Utilisation (CGU) régissent l'utilisation de l'application mobile Kêkênon (ci-après « l'Application »), éditée par YUPI Global, pour les services de transport de personnes en République du Bénin.</p>

    <h2>2. Acceptation des CGU</h2>
    <p>En créant un compte et en utilisant l'Application, l'utilisateur accepte sans réserve les présentes CGU. Si vous n'acceptez pas ces conditions, veuillez ne pas utiliser l'Application.</p>

    <h2>3. Description du service</h2>
    <p>Kêkênon est une plateforme de mise en relation entre des passagers souhaitant effectuer un trajet et des chauffeurs partenaires. Le service comprend :</p>
    <ul>
        <li>La commande de courses à la demande</li>
        <li>Le suivi en temps réel du véhicule</li>
        <li>Le paiement via portefeuille électronique</li>
    </ul>

    <h2>4. Inscription et compte utilisateur</h2>
    <h3>4.1 Création de compte</h3>
    <p>L'inscription nécessite un numéro de téléphone valide au Bénin. Un code OTP sera envoyé par SMS pour vérification. L'utilisateur doit avoir au moins 18 ans.</p>
    <h3>4.2 Responsabilité du compte</h3>
    <p>L'utilisateur est responsable de la confidentialité de son compte et de toutes les actions effectuées depuis celui-ci.</p>

    <h2>5. Tarification</h2>
    <p>Les tarifs sont calculés automatiquement en fonction de la distance, du type de véhicule et des conditions de circulation. Le prix est affiché avant la confirmation de la course. Les tarifs sont exprimés en Francs CFA (XOF).</p>

    <h2>6. Paiement</h2>
    <p>Les paiements s'effectuent via le portefeuille intégré, rechargeable par Mobile Money (MTN, Moov) ou autres moyens de paiement disponibles. Aucun paiement en espèces n'est encouragé via l'Application.</p>

    <h2>7. Obligations de l'utilisateur</h2>
    <p>L'utilisateur s'engage à :</p>
    <ul>
        <li>Fournir des informations exactes et à jour</li>
        <li>Respecter le chauffeur et le véhicule</li>
        <li>Ne pas utiliser l'Application à des fins illicites</li>
        <li>Être présent au point de prise en charge à l'heure convenue</li>
        <li>Ne pas transporter de substances illicites ou d'objets dangereux</li>
    </ul>

    <h2>8. Obligations du chauffeur partenaire</h2>
    <p>Le chauffeur partenaire s'engage à :</p>
    <ul>
        <li>Posséder un permis de conduire valide et une assurance en cours</li>
        <li>Maintenir son véhicule en bon état</li>
        <li>Respecter le Code de la route béninois</li>
        <li>Traiter les passagers avec respect et courtoisie</li>
        <li>Suivre l'itinéraire indiqué sauf indication contraire du passager</li>
    </ul>

    <h2>9. Annulation</h2>
    <p>Le passager peut annuler une course gratuitement avant l'arrivée du chauffeur. Le chauffeur peut annuler une course sous certaines conditions. Des frais d'annulation peuvent s'appliquer selon les circonstances.</p>

    <h2>10. Évaluation et notation</h2>
    <p>Après chaque course, passagers et chauffeurs peuvent s'évaluer mutuellement. Un comportement inapproprié répété peut entraîner la suspension ou la suppression du compte.</p>

    <h2>11. Responsabilité</h2>
    <p>Kêkênon agit en tant qu'intermédiaire technologique. La responsabilité du transport incombe au chauffeur partenaire. Kêkênon ne saurait être tenu responsable des dommages directs ou indirects liés au transport.</p>

