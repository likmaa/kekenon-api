        <tr><td>Photo de profil</td><td>Identification visuelle</td><td>Non</td></tr>
        <tr><td>Email</td><td>Communications</td><td>Non</td></tr>
    </table>

    <h3>2.2 Données collectées automatiquement</h3>
    <table>
        <tr><th>Donnée</th><th>Finalité</th></tr>
