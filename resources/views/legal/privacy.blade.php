    <h3>2.3 Données spécifiques aux chauffeurs</h3>
    <ul>
        <li>Pièce d'identité et permis de conduire</li>
        <li>Carte grise du véhicule</li>
        <li>Certificat d'assurance</li>
        <li>Localisation en temps réel (pendant les heures de service)</li>
    </ul>

    <h2>3. Utilisation des données</h2>
    <p>Vos données sont utilisées exclusivement pour :</p>
    <ul>
        <li>Fournir et améliorer le service de transport</li>
        <li>Mettre en relation passagers et chauffeurs</li>
        <li>Gérer les paiements et le portefeuille électronique</li>
        <li>Assurer la sécurité des utilisateurs</li>
        <li>Envoyer des notifications de service (courses, paiements)</li>
        <li>Résoudre les litiges et fournir un support client</li>
    </ul>

    <h2>4. Partage des données</h2>
    <p>Vos données ne sont jamais vendues à des tiers. Elles peuvent être partagées avec :</p>
    <ul>
        <li><strong>Les chauffeurs/passagers</strong> : nom et position pour le bon déroulement de la course</li>
        <li><strong>Les prestataires de paiement</strong> : pour le traitement des transactions (PawaPay, opérateurs Mobile Money)</li>
        <li><strong>Les autorités</strong> : sur demande légale dûment fondée</li>
    </ul>

    <h2>5. Stockage et sécurité</h2>
    <p>Vos données sont stockées sur des serveurs sécurisés. Nous mettons en œuvre des mesures techniques et organisationnelles pour protéger vos données :</p>
    <ul>
        <li>Chiffrement des communications (TLS/HTTPS)</li>
        <li>Authentification par token sécurisé</li>
        <li>Accès restreint aux données selon le principe du moindre privilège</li>
        <li>Sauvegardes régulières</li>
    </ul>

    <h2>6. Conservation des données</h2>
    <p>Les données sont conservées pendant la durée d'utilisation du compte, plus une période de 12 mois après la suppression du compte pour des obligations légales et comptables. Les données de transaction sont conservées 5 ans conformément aux obligations fiscales.</p>

    <h2>7. Vos droits</h2>
    <p>Vous disposez des droits suivants :</p>
    <ul>
        <li><strong>Droit d'accès</strong> : consulter vos données personnelles</li>
        <li><strong>Droit de rectification</strong> : corriger des informations inexactes</li>
        <li><strong>Droit de suppression</strong> : demander l'effacement de vos données</li>
        <li><strong>Droit à la portabilité</strong> : recevoir vos données dans un format structuré</li>
        <li><strong>Droit d'opposition</strong> : vous opposer au traitement de vos données pour des motifs légitimes</li>
    </ul>
    <p>Pour exercer ces droits, contactez-nous à <a href="mailto:support@kekenon.com">support@kekenon.com</a>.</p>

    <h2>8. Géolocalisation</h2>
    <p>L'Application utilise la géolocalisation pour :</p>
    <ul>
        <li>Déterminer votre position pour commander une course</li>
        <li>Suivre le trajet en temps réel</li>
        <li>Calculer la distance et le tarif</li>
    </ul>
    <p>Vous pouvez désactiver la géolocalisation dans les paramètres de votre téléphone, mais cela rendra l'Application inutilisable.</p>

    <h2>9. Notifications</h2>
    <p>Nous envoyons des notifications push pour les événements liés à vos courses et votre portefeuille. Vous pouvez gérer vos préférences de notification dans les paramètres de l'Application.</p>

    <h2>10. Cookies et technologies similaires</h2>
    <p>L'Application mobile n'utilise pas de cookies. Le site web peut utiliser des cookies techniques nécessaires au bon fonctionnement.</p>

