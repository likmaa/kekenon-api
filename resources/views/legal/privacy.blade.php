    <h2>1. Introduction</h2>
    <p>La présente Politique de Confidentialité décrit comment YUPI Global (« nous », « notre ») collecte, utilise, stocke et protège les données personnelles des utilisateurs de l'application Kêkênon.</p>
    <p>Nous nous engageons à protéger votre vie privée conformément à la réglementation en vigueur en République du Bénin et aux meilleures pratiques internationales.</p>

    <h2>2. Données collectées</h2>
